% script to reset default parameters of figure display (especially useful
% for SPM display which otherwise looks a bit weird when using my own
% default parameters for other figures)

set(0,'defaultLineLineWidth',"default"); % set the default line width to lWidth
set(0,'defaultLineMarkerSize',"default"); % set the default line marker size to mSize
set(0,'defaultTextFontName',"default"); % set the default police font name
set(0,'defaultTextFontSize',"default"); % set the default police font size

% set axes features
set(0,'defaultAxesFontName',"default"); % set the default police font name
set(0,'defaultAxesFontSize',"default"); % set the default police font size

% set legend features
set(0,'defaultLegendFontName',"default"); % set the default police font size for legends
set(0,'defaultLegendFontSize',"default"); % set the default police font size for legends
